let A = prompt("Введите значение для A:");
let B = prompt("Введите значение для B:");
let C = prompt("Введите значение для C:");
let D = prompt("Введите значение для D:");
let E = prompt("Введите значение для E:");

// Сохраняем начальное значение A для перестановки
let temp = A;

// Перестановка
A = E; // Значение A переходит в E
E = B; // Значение E переходит в B
B = D; // Значение B переходит в D
D = C; // Значение D переходит в C
C = temp; // Значение C получает начальное значение A

console.log(`Новые значения: A=${A}, B=${B}, C=${C}, D=${D}, E=${E}`);
